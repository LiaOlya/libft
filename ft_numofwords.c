#include "libft.h"

int		ft_numofwords(char const *s, char c)
{
	int		i[4];

	i[0] = 0;
	i[2] = ft_strlen(s);
	while (s[i[0]] == c)
		++i[0];
	while (s[i[2]] == c)
		--i[2];
	i[2] -= i[0];
	i[0] = 0;
	i[1] = 1;
	while (i[0] < i[2])
	{
		i[3] = 0;
		while (s[i[0]] == c)
		{
			if (i[3] == 0)
				++i[1];
			i[3] = 1;
			++i[0];
		}
		++i[0];
	}
	return (i[1]);
}
